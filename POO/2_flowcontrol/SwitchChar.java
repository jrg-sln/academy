public class SwitchChar {
     public static void main (String [] args) {
	char caracter = 'a';
	switch(caracter){
		case 'a':
			System.out.println("Se eligiO A.");
			break;
		case 'b':
			System.out.println("Se eligiO B.");
			break;
 		default:
			System.out.println("OpciOn invAlida.");
		}
	}
}

